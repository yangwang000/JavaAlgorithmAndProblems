public class BitManipulation {
	// 0s 1s means sequence of 0 or 1
	// x ^ 0s = x; x ^ 1s = ~x; x ^ x = 0;
	// x & 0s = 0; x & 1s = x; x & x = x;
	// x | 0s = x; x | 1s = 1s; x | x = x;
	// 0110+0110 is equivalent to 0110*2 shift left by 1
	// 0110*0100 shift 0110 left by 2
	// a^(~a) = 1s
}
